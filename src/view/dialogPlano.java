    private void carregaCombo(){
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(Assinatura.values());
        comboAssinatura.setModel(cbm);
    }
    
    private void limpaComponentes(){
        textId.setText("");
        textNome.setText("");
        spinnerValor.setValue(0.01);
        comboAssinatura.setSelectedIndex(0);
    }
    
    private Plano criaObjeto(){
        Plano p = new Plano();
        p.setId(textId.getText().trim().isEmpty() ? 0 : Integer.parseInt(textId.getText()));
        p.setNome(textNome.getText());
        p.setValor((Double)spinnerValor.getValue());
        p.setAssinatura((Assinatura)comboAssinatura.getSelectedItem());
        return p;
    }
    
    private void populaComponentes(Plano p){
        textId.setText(p.getId()+"");
        textNome.setText(p.getNome());
        spinnerValor.setValue(p.getValor());
        comboAssinatura.setSelectedItem(p.getAssinatura());
    }
    
    private boolean camposValidos(){
        return (!textNome.getText().trim().isEmpty());
    }
    
