    private void carregaTable(){
        tablePesquisa.setModel(new MyTableModel(Equipamento.class, dao.read(), tablePesquisa));
    }
    
    private void carregaTable(String filtro){
        tablePesquisa.setModel(new MyTableModel(Equipamento.class, dao.readByNome(filtro), tablePesquisa));
    }
    
    private void limpaComponentes(){
        textId.setText("");
        textNome.setText("");
        spinnerValor.setValue(0.01);
        textData.setText("");
    }
    
    private Equipamento criaObjeto(){
        Equipamento e = new Equipamento();
        e.setId(textId.getText().trim().isEmpty() ? 0 : Integer.parseInt(textId.getText()));
        e.setNome(textNome.getText());
        e.setValor((Double)spinnerValor.getValue());
        e.setCompra(LocalDate.parse(textData.getText(), formato));
        return e;
    }
    
    private void populaComponentes(Equipamento e){
        textId.setText(e.getId()+"");
        textNome.setText(e.getNome());
        spinnerValor.setValue(e.getValor());
        textData.setValue(e.getCompra().format(formato));
    }
    
    private boolean camposValidos(){
        return (!textNome.getText().trim().isEmpty() && !textData.getText().equals("  /  /    "));
    }
    
